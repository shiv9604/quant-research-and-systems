// Dates in javascript

var date =  new Date();
console.log(date); 
console.log(date.getTime());
console.log(date.getDay()); // Returns the value of day in a week in numbers
console.log(date.getFullYear()); //returns the current year value
console.log(date.getHours()); // returns the current hour value
console.log(date.getMonth()); // returns the current month value
